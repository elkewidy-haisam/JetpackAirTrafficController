/**
 * Represents a designated landing and storage zone for jetpack aircraft.
 * 
 * Purpose:
 * Models a physical parking location within a city where jetpacks can safely land, be stored, and
 * launch from. Tracks occupancy status to support resource allocation and prevent double-booking.
 * Provides spatial coordinates for rendering parking zones on maps and calculating proximity during
 * landing operations.
 * 
 * Key Responsibilities:
 * - Maintain unique identifier for parking space registry and reference
 * - Store immutable geographic coordinates (x, y) for precise location
 * - Track occupancy state (occupied/vacant) for availability checks
 * - Provide occupy() and vacate() operations for state transitions
 * - Support string representation for logging and display
 * 
 * Interactions:
 * - Managed by ParkingSpaceManager for allocation and availability queries
 * - Generated by ParkingSpaceGenerator for procedural city infrastructure
 * - Displayed on CityMapPanel and MapDisplayPanel as visual markers
 * - Referenced in City model as part of urban infrastructure
 * - Used in flight landing logic to determine valid destination zones
 * 
 * Patterns & Constraints:
 * - Immutable location ensures spatial integrity; position cannot change after creation
 * - Mutable occupancy state supports real-time allocation updates
 * - Lightweight value object suitable for large-scale parking networks
 * - Thread-safe reads; synchronized writes required for occupancy at higher layers
 * 
 * @author Haisam Elkewidy
 */

package com.example.model;

public class ParkingSpace {
    private final String id;
    private final double x;
    private final double y;
    private boolean occupied;

    public ParkingSpace(String id, double x, double y) {
        this.id = id;
        this.x = x;
        this.y = y;
        this.occupied = false;
    }

    public ParkingSpace(String id, int x, int y) {
        this(id, (double)x, (double)y);
    }

    public String getId() {
        return id;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public boolean isOccupied() {
        return occupied;
    }

    public void occupy() {
        this.occupied = true;
    }

    public void vacate() {
        this.occupied = false;
    }

    @Override
    public String toString() {
        return String.format("ParkingSpace[id=%s, x=%.3f, y=%.3f, occupied=%s]", id, x, y, occupied);
    }
}
